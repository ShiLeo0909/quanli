package frameworkjava;

public class CollectionFramework {
    public static void main(String[] args) {
        //Collection Framework trong Java cung cấp một tập hợp các lớp và giao diện để lưu trữ,
        // quản lý và truy cập các đối tượng trong chương trình Java. Nó bao gồm các thành phần sau:
        //
        //Interfaces:

        //Collection: đại diện cho tập hợp các đối tượng, bao gồm List, Set và Queue.
        //List: đại diện cho danh sách các đối tượng, cho phép truy cập các phần tử bằng chỉ số.
        //Set: đại diện cho tập hợp các đối tượng không có thứ tự và không chứa các phần tử trùng lặp.
        //Queue: đại diện cho hàng đợi các đối tượng.

        //Classes:

        //ArrayList: triển khai giao diện List bằng cách sử dụng một mảng.
        //LinkedList: triển khai giao diện List bằng cách sử dụng danh sách liên kết.
        //HashSet: triển khai giao diện Set bằng cách sử dụng bảng băm (hash table).
        //TreeSet: triển khai giao diện Set bằng cách sử dụng một cây nhị phân tìm kiếm.
        //PriorityQueue: triển khai giao diện Queue bằng cách sử dụng một heap.
        //Algorithms:
        //Collection Framework cung cấp một số thuật toán để thao tác với các đối tượng trong Collection như searching,
        // sorting, shuffling, và reversing.
        //
        //Map Interface:
        //Map: một giao diện để thao tác với các cặp key-value.
        //Map Classes:
        //HashMap: một Map triển khai bằng bảng băm.
        //TreeMap: một Map triển khai bằng cây tìm kiếm nhị phân.
        //Miscellaneous:
        //
        //Arrays: cung cấp một số phương thức để thao tác với các mảng.
        //Iterator: đại diện cho một vòng lặp để duyệt qua các phần tử trong một Collection.
        //Comparator: đại diện cho một đối tượng được sử dụng để so sánh các đối tượng trong một Collection.
        //Collection Framework trong Java cung cấp cho chúng ta một cách tiện lợi và hiệu quả để lưu trữ và quản lý các đối tượng,
        // đồng thời cũng cung cấp một số thuật toán và tính năng mạnh mẽ để thao tác với chúng.
    }
}
